package edu.ucsb.nceas.morpho.framework;

/**
 * This exception is thrown when a plug registers a service under a 
 * serviceName that has previously been registered.  The service must
 * be re-registered under a different name in order to be utilized.
 */
public class ServiceExistsException extends Exception
{
  public ServiceExistsException(String message)
  {
    super(message);
  }
}
